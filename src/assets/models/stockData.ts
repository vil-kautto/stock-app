/**
 * StockData contains all the stock data used by the application
 */
export class StockData {
/**
 * date of given stock
 */
    public date: any;
/**
 * closing value of the stock
 */
    public close: any;
/**
 * daily trading volume of the stock
 */
    public volume: any;
/**
 * opening value of the stock
 */
    public open: any;
/**
 * daily high value of the stock
 */
    public high: any;
/**
 * daily low value of the stock
 */
    public low: any;
/**
 * daily change in the value of the stock
 */
    public change: any;
}